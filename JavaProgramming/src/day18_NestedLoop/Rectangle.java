package day18_NestedLoop;

public class Rectangle {
    public static void main(String[] args) {

        char ch = '*';

        for (int j = 0; j < 10 ; j++) {
            for (int i = 0; i < 10; i++) {
                System.out.print(ch + " ");
            }
            System.out.println();


        }

    }
}
